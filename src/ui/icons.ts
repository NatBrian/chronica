/** faction sigils (doc 14 D4): banner identity repeated across the UI */
export const FACTION_SIGIL = ['crown', 'sprout', 'pick', 'paw', 'fire', 'snow', 'ring', 'star'];

/** Heraldic pennant: faction-colored banner bearing its sigil. */
export function bannerSvg(color: string, factionId: number, size = 15): string {
  const sig = G[FACTION_SIGIL[factionId & 7]] ?? G.star;
  let d = '';
  for (let y = 0; y < sig.length; y++) {
    for (let x = 0; x < sig[y].length; x++) {
      if (sig[y][x] === '#') d += `M${x} ${y}h1v1h-1z`;
    }
  }
  return `<svg class="pxi" viewBox="0 0 12 15" width="${size * 0.8}" height="${size}" aria-hidden="true">`
    + `<path d="M0 0h12v11l-6 4-6-4z" fill="${color}" stroke="#0f0d16" stroke-width="0.7"/>`
    + `<g transform="translate(1.6,1.4) scale(0.9)"><path d="${d}" fill="#0f0d16" fill-opacity="0.82"/></g></svg>`;
}

const cache: Record<string, string> = {};

/** Inline SVG for a named icon; fills with currentColor so CSS drives it. */
export function iconSvg(name: string, size = 13): string {
  const key = `${name}:${size}`;
  if (cache[key]) return cache[key];
  const g = G[name];
  if (!g) return '';
  let d = '';
  for (let y = 0; y < g.length; y++) {
    for (let x = 0; x < g[y].length; x++) {
      if (g[y][x] === '#') d += `M${x} ${y}h1v1h-1z`;
    }
  }
  const svg = `<svg class="pxi" viewBox="0 0 10 10" width="${size}" height="${size}" aria-hidden="true"><path d="${d}" fill="currentColor"/></svg>`;
  cache[key] = svg;
  return svg;
}

/** HTML for an event/UI glyph: SVG icon when mapped, escaped text otherwise. */
export function glyphHtml(glyph: string, size = 13): string {
  const name = GLYPH_ICON[glyph];
  if (name) return iconSvg(name, size);
  return glyph.replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;');
}
